function Scope(width, height){
  this.width = width,
  this.height = height,
  this.stringified = this.clear()
};

Scope.prototype.clear = function() {
  var scope = '';
  for (var i = 1; i <= this.height; i++) {
    for (var j = 1; j <= this.width; j++) {
      scope += '.';
      if (j === this.width) {
        scope += '\n';
      }
    }
  }
  return scope;
};

